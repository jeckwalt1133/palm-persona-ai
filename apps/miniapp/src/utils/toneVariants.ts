export function getSharpCoreTruth(personaType: string): string | null {
  return SHARP_CORE_TRUTH[personaType] ?? null;
}

export function getSharpInsight(insightText: string): string | null {
  // 尝试精确匹配，失败则返回null
  for (const pool of Object.values(SHARP_INSIGHTS)) {
    const idx = pool.findIndex((s) => s.startsWith(insightText.slice(0, 10)));
    if (idx !== -1) return pool[idx];
  }
  return null;
}

// 映射温和洞察→犀利洞察（通过pool位置）
function findSharpInsightByMild(mildText: string): string | null {
  // 搜索所有 persona type 的 insight pool
  // 直接遍历 SHARP_INSIGHTS，匹配开头
  for (const pool of Object.values(SHARP_INSIGHTS)) {
    for (let i = 0; i < pool.length; i++) {
      // 温和版和犀利版共享同一个索引位置
      // 这里我们用模糊匹配：尝试匹配文本的前8个字
      if (pool[i].startsWith(mildText.slice(0, 8))) {
        return pool[i];
      }
    }
  }
  return null;
}

export function applyToneToInsight(insight: string, tone: ToneMode): string {
  if (tone === 'mild') return insight;
  const sharp = findSharpInsightByMild(insight);
  return sharp ?? insight;
}

export function applyToneToCoreTruth(coreTruth: string, personaType: string, tone: ToneMode): string {
  if (tone === 'mild') return coreTruth;
  return getSharpCoreTruth(personaType) ?? coreTruth;
}

export function applyToneToSignalPattern(pattern: string, tone: ToneMode): string {
  if (tone === 'mild') return pattern;
  for (const [key, val] of Object.entries(SHARP_SIGNAL_PATTERNS)) {
    if (pattern.includes(key)) return val;
  }
  return pattern;
}

export function applyToneToTensionPoint(tension: string, tone: ToneMode): string {
  if (tone === 'mild') return tension;
  for (const [key, val] of Object.entries(SHARP_TENSION_POINTS)) {
    if (tension.includes(key)) return val;
  }
  return tension;
}

