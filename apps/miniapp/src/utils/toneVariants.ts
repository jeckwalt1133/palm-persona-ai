export type ToneMode = 'mild' | 'sharp';

const STORAGE_KEY = 'palm_tone_mode';

export function getStoredTone(): ToneMode {
  try {
    const v = localStorage?.getItem(STORAGE_KEY);
    if (v === 'sharp') return 'sharp';
  } catch { /* H5 only */ }
  return 'mild';
}

export function setStoredTone(mode: ToneMode): void {
  try { localStorage?.setItem(STORAGE_KEY, mode); } catch { /* noop */ }
}

